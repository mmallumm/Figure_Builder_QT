#include "adddialog.h"
#include "ui_adddialog.h"
#include "app.h"


//--------------------------------------------------------------------------------------//
AddDialog::AddDialog(App &app, QWidget *parent) :
    QDialog(parent),
    ui(new Ui::AddDialog)
{
    ui->setupUi(this);

    this->app = &app;

    connect(this, SIGNAL(dialogAccepted()), &app, SLOT(addCoords()));
}
//--------------------------------------------------------------------------------------//


//--------------------------------------------------------------------------------------//
AddDialog::~AddDialog()
{
    delete ui;
}
//--------------------------------------------------------------------------------------//


//--------------------------------------------------------------------------------------//
void AddDialog::on_buttonBox_accepted()
{
    emit dialogAccepted();
}
//--------------------------------------------------------------------------------------//

